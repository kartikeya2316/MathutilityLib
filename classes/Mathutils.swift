import Foundation

public class Mathutils{
    public init(){}
   public func add(a:Int,b:Int)->Int{
        return a+b
    }
  public  func multiply(a:Int,b:Int)->Int{
        return a*b
    }
 public   func div(a:Int,b:Int)->Int{
        return a/b
    }
}
